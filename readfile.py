# Запрашиваем у пользователя имя файла
filename = input("Введите имя файла для чтения: ")

try:
    # Используем конструкцию 'with' для безопасной работы с файлом.
    # Файл будет автоматически закрыт после выхода из блока 'with'.
    with open(filename, 'r', encoding='utf-8') as file:
        content = file.read()
        print("\n--- Содержимое файла ---")
        print(content)
        print("------------------------")

except FileNotFoundError:
    # Обработка случая, когда файл не существует по указанному пути
    print(f"Ошибка: Файл '{filename}' не найден. Проверьте правильность пути и имени файла.")

except PermissionError:
    # Обработка случая, когда у программы нет прав для чтения файла
    print(f"Ошибка: Отказано в доступе. У программы нет прав для чтения файла '{filename}'.")

except Exception as e:
    # Общая обработка любых других непредвиденных ошибок, которые могут возникнуть
    print(f"Произошла непредвиденная ошибка при работе с файлом: {e}")

finally:
    # Этот блок выполняется всегда, независимо от того, была ли ошибка или нет
    print("Операция чтения файла завершена.")